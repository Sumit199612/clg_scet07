                    <p style="text-align: justify;"><span style="font-family: georgia, palatino, serif;"><span style="color: #0d0d0d;"><h3>Punishments for those found guilty of ragging at the institution level</h3></p>
                    <p style="text-align: justify;"><span style="font-size: 10pt; font-family: georgia, palatino, serif;"><span style="color: #0d0d0d;">Depending upon the nature and gravity of the offence as established the possible punishments for those found guilty of ragging at the institution level shall be any one or any combination of the following:-</p>
                    <p style="text-align: justify;"><span style="font-size: 10pt; font-family: georgia, palatino, serif;"><span style="color: #0d0d0d;">(i)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Cancellation of admission</p>
                    <p style="text-align: justify;"><span style="font-size: 10pt; font-family: georgia, palatino, serif;"><span style="color: #0d0d0d;">(ii)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Suspension from attending classes</p>
                    <p style="text-align: justify;"><span style="font-size: 10pt; font-family: georgia, palatino, serif;"><span style="color: #0d0d0d;">(iii)&nbsp; &nbsp;&nbsp; Withholding/withdrawing scholarship/fellowship and other benefits</p>
                    <p style="text-align: justify;"><span style="font-size: 10pt; font-family: georgia, palatino, serif;"><span style="color: #0d0d0d;">(iv)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Debarring from appearing in any test/examination or other evaluation process</p>
                    <p style="text-align: justify;"><span style="font-size: 10pt; font-family: georgia, palatino, serif;"><span style="color: #0d0d0d;">(v)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Withholding results</p>
                    <p style="text-align: justify;"><span style="font-size: 10pt; font-family: georgia, palatino, serif;"><span style="color: #0d0d0d;">(vi)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Debarring from representing the institution in any regional, national or international meet, tournament, youth festival, etc.</p>
                    <p style="text-align: justify;"><span style="font-size: 10pt; font-family: georgia, palatino, serif;"><span style="color: #0d0d0d;">(vii)&nbsp;&nbsp;&nbsp; Suspension/expulsion from the hostel</p>
                    <p style="text-align: justify;"><span style="font-size: 10pt; font-family: georgia, palatino, serif;"><span style="color: #0d0d0d;">(viii)&nbsp;&nbsp; Rustication from the institution for period ranging from 1 to 4 semesters</p>
                    <p style="text-align: justify;"><span style="font-size: 10pt; font-family: georgia, palatino, serif;"><span style="color: #0d0d0d;">(ix)&nbsp;&nbsp;&nbsp;&nbsp;&nbsp; Expulsion from the institution and consequent debarring from admission to any other institution.</p>
                    <p style="text-align: justify;"><span style="font-size: 10pt; font-family: georgia, palatino, serif;"><span style="color: #0d0d0d;">(x)&nbsp; &nbsp; &nbsp; Collective punishment: when the persons committing or abetting the crime of ragging are not identified, the institution shall resort to collective punishment as a deterrent to ensure community pressure on the potential raggers.</p>
                    <p style="text-align: justify;"><span style="font-size: 10pt; font-family: georgia, palatino, serif;"><span style="color: #0d0d0d;">2. Student can also meet following faculties regarding ragging issues&nbsp;<strong>between 3:00 to 4:00 pm</strong>&nbsp;at respective departments.</p>
